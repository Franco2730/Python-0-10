# Como vimos, nosotros elegimos el nombre de nuestras cajas (variables), pero es altamente recomendable poner un nombre que se asemeje a su contenido Ej:
nombre_perro = "Simba"

# Más no colocar algo como lo siguiente, la realidad es que el editor de códivo lo va a leer sin problemas, pero nosotros vamos a tener dificultad para saber para que era esa variable o que relacion tiene con el programa.
