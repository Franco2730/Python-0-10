# El nombre de la variable tiene que ser una sola palabra, es decir si queremos usar dos palabras, necesitamos usar guion bajo en cambio del espacio:
Estacion_favorita = "Invierno"

# Dichos nombres no aceptan la letra ñ ni acentos. Ej: Anio = 2024
