a = 6
b = 2
c = 7

print(f"{a} más {b} es igual a: { a + b }")                 # 6 más 2 es igual a: 8
print(f"{a} menos {b} es igual a: { a - b }")               # 6 menos 2 es igual a: 4
print(f"{a} por {b} es igual a: { a * b }")                 # 6 por 2 es igual a: 12
print(f"{a} dividido {b} es igual a: { a / b }")            # 6 dividido 2 es igual a: 3

print(f"{a} dividido al piso de {b} es igual a: { a // b }")# 6 dividido al piso de 2 es igual a: 3
print(f"{a} modulo de {b} es igual a: { c % b }")           # 6 modulo de 2 es igual a: 1
print(f"{a} elevado a la {3} es igual a: { a ** 3 }")       # 6 elevado a la 3 es igual a: 216
print(f"La raiz cuadrada de {a} es {a ** 0.5}")             # La raiz cuadrada de 6 es 2.449489742783178