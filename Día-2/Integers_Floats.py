# Como ya habíamos dicho, podemos por supuesto agregar INT en variables y hacer distintas cosas con esto. 

mi_numero = 1 
print(mi_numero) # 1

otro_numero = 5.8

mi_suma = 10 + 20
print(mi_suma) # 30

# Tenemos una herramienta muy útil que es TYPE. Con este método podemos determinar que tipo de dato es el valor de nuestra variable.  

print(type(mi_numero)) # <class 'int'>
print(type(otro_numero)) # <class 'float'>

