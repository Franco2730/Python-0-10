# Esto es otro pilar de la programación. Loop significa bucle, esto sirve para que un bloque de código se repita una y otra vez escribiendo una sola vez el código. 
# Esto es realmente útil en muchos casos. Por ejemplo: Queremos que nuestro personaje 
#* Dispare su arma una cantidad indefinida de veces hasta que una condicion se cumpla. Podriamos decir algo como:

municiones = 10

if municiones > 0:
    municiones = municiones - 1

# Ya verémos otro concepto muy importante que esta estrechamente vinculado con los bucles que es el concepto de algo ITERABLE. Que significa esto, veamoslo con un ejemplo. Supongamos que tenemos una lista llamada mi_lista que contiene 5 elementos.

mi_lista = [1, 2, 3, 4, 5]

#* Una lista es iterable, porque podemos ejecutar un loop (un bucle) para que haga una repeticion por cada elemento de la lista. 

# Por ejemplo podemos crear un codigo que por cada iteracion (cada vuelta que de el bucle) se imprima cada uno de los elementos de la lista de uno en uno hasta que esta se agote. 


